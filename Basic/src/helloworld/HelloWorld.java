package helloworld;

public class HelloWorld {

    public static void main(String[] args) {
        System.out.println("Hello World!");
        System.out.println("Welcome to Java!");
    }
}

/*一个源文件至多声明一个public类
* 一个源文件可以包含多个类class
* main()方法是程序的入口
* 每个语句以;结束*/

class TwoClass{
    public static void main(String[] args) {
        System.out.println("这是第二个类.");
        System.out.println("测试上传!");
    }
}
