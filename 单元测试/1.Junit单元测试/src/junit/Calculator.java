package junit;

/**
 * 计算器类
 * @Author: Harlan
 * @Date: 2020/6/24 8:50
 */
public class Calculator {

    /**
     * 加法
     * @param a 被加数
     * @param b 加数
     * @return 和
     */
    public double add(double a, double b){
        return  a + b;
    }

    /**
     * 减法
     * @param a 被减数
     * @param b 减数
     * @return 和
     */
    public double sub(double a, double b){
        return a - b;
    }
}
